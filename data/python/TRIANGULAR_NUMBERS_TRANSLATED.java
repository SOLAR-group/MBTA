public static boolean f_gold ( int num ) {
  if ( ( num < 0 ) || ( num > num ) ) return false ;
  int sum = 0 , n = 1 ;
  while ( ( sum <= num ) && ( sum != num ) ) {
    sum = sum + n ;
    if ( ( sum == num ) && ( sum != num ) ) return true ;
    n ++ ;
  }
  return false ;
}

